const sleep = (ms) => new Promise(resolve => setTimeout(resolve, ms));

function setNativeValue(element, value) {
  const prototype = Object.getPrototypeOf(element);
  const valueSetter = Object.getOwnPropertyDescriptor(prototype, "value")?.set;

  if (!valueSetter) {
    element.value = value;
  } else {
    valueSetter.call(element, value);
  }

  element.dispatchEvent(new InputEvent("input", {
    bubbles: true,
    inputType: "insertText",
    data: value
  }));

  element.dispatchEvent(new Event("change", { bubbles: true }));
}

function getTextarea() {
  return document.querySelector('textarea[aria-label="Manual idea block input"]');
}

function getAddButton() {
  return [...document.querySelectorAll("button")]
    .find(btn => btn.textContent.trim() === "新增" || btn.textContent.includes("新增"));
}

async function waitForEnabledAddButton(timeout = 5000) {
  const start = Date.now();

  while (Date.now() - start < timeout) {
    const addButton = getAddButton();

    if (addButton && !addButton.disabled && !addButton.getAttribute("aria-disabled")) {
      return addButton;
    }

    await sleep(100);
  }

  throw new Error("找不到可點擊的「新增」按鈕。可能 textarea 沒有成功觸發 input，或按鈕文字不是「新增」。");
}

async function waitForTextareaClearOrStable(textarea, previousValue, timeout = 3000) {
  const start = Date.now();

  while (Date.now() - start < timeout) {
    if (textarea.value === "" || textarea.value !== previousValue) {
      return;
    }

    await sleep(100);
  }
}

async function addIdeas() {
  const textarea = getTextarea();

  if (!textarea) {
    throw new Error('找不到 textarea[aria-label="Manual idea block input"]');
  }

  console.log(`準備新增 ${ideas.length} 筆 idea block。`);

  for (let i = 0; i < ideas.length; i++) {
    const idea = ideas[i];

    textarea.focus();
    setNativeValue(textarea, idea);

    await sleep(150);

    const addButton = await waitForEnabledAddButton();
    addButton.click();

    console.log(`已新增 ${i + 1}/${ideas.length}：${idea}`);

    await waitForTextareaClearOrStable(textarea, idea);
    await sleep(300);
  }

  console.log(`完成，共新增 ${ideas.length} 筆 idea block。`);
}

addIdeas();